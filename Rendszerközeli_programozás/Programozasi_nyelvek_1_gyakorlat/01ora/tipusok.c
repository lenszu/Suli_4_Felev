#include <stdio.h>

int main()
{
    int szam = 2022;
    printf("szam: %d\n", szam);

    char betu = 'H';
    printf("betu: %c\n", betu);

    float lebego = 3.14159;
    printf("lebego: %f\n", lebego);

    double dupla_lebego = 1.1234567;
    printf("dupla lebego: %lf\n", dupla_lebego);

    return 0;
}